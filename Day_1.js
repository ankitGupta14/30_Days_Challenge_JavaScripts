// Activity 3: Data Types

// Task 4: Create variables of different data types (number, string, boolean, object, array) and log each variable's type using the typeof operator.

// Number
const KillAge = 34
console.log(typeof KillAge)

// String
const KillName = "Kill"
console.log(typeof KillName)

// Boolean
const killTruth = true
console.log(typeof killTruth)

// Object
const KillPerson ={
    name: "Kill",
    age: 34,
    isAlive: true
}
console.log(typeof KillPerson)

// Array 
const Fruits = ["apple", "orange", "banana"]
console.log(typeof Fruits)

